package com.example.adapter_pattern.dto;

/**
 * User 엔티티: 사용자 정보를 나타내는 클래스이다.
 * 해당 클래스는 id, name, email 필드를 가지고 있다.
 */

public class User {
    private String id;
    private String name;
    private String email;

    public User(String id, String name, String email) {
        this.id = id;
        this.name = name;
        this.email = email;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

}
